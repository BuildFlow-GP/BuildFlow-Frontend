class User {
  final String name;
  final String email;
  final String phone;
  final String idNumber;
  final String bankAccount;
  final String location;
  final String profileImageUrl;

  User({
    required this.name,
    required this.email,
    required this.phone,
    required this.idNumber,
    required this.bankAccount,
    required this.location,
    required this.profileImageUrl,
  });
}
